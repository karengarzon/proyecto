    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        
        
        
        if(Utils.checkEditTexts(etCodigoProducto,etCostoProducto,etCosto1,etUtilidad1,etPrecioVenta1,etImpuesto1
        ,etCosto2,etUtilidad2,etPrecioVenta2,etImpuesto2
        ,etCosto3,etUtilidad3,etPrecioVenta3,etImpuesto3
        ,etCantidadMinima,etCantidadBodega,etNombreProducto)){
            
            
            Proveedor p = listaProveedores.get(comboProveedores.getSelectedIndex());
            
            
            String codigo = etCodigoProducto.getText();
            String descripcion = etDescripcionProducto.getText();
            String nombre = etNombreProducto.getText();
            float costo = Float.parseFloat(etCostoProducto.getText());
            float utilidad_1= Float.parseFloat(etUtilidad1.getText());
            float utilidad_2= Float.parseFloat(etUtilidad2.getText());
            float utilidad_3= Float.parseFloat(etUtilidad3.getText());
            String cod_proveedor = p.getCodigo();
            float cantidad_almacen = Integer.parseInt(etCantidadBodega.getText());
            float cantidad_minima  = Integer.parseInt(etCantidadMinima.getText());
            int tipo_producto = 0;
            if(radioServicio.isSelected()){
               tipo_producto = 1; 
            }
            
            String foto_producto = "null";
            
            Producto mProducto = new Producto(codigo,nombre,descripcion,costo,utilidad_1,utilidad_2,utilidad_3,cod_proveedor,cantidad_almacen,cantidad_minima,tipo_producto,foto_producto);
            
            
            DataBaseHelper db = new DataBaseHelper();
                
                //Conectar a la BAse de DAtos
            db.Conectar();
                    
            boolean exito = db.insertarProducto(mProducto);
            
            
            
            
            if(exito){
                    
                    int opcion = Utils.showYesNoDialog(this,"Se ha guardado el Producto, ¿Desea crear otro Producto?");
                    
                    if(opcion == 0){
                        jButton2ActionPerformed(null);
                    }else{
                        Utils.cerrarVentana(this);
                    }
                    
                }else{
                    
                    Utils.showDialog(this, "Ocurrio un error al guardar el Producto", "Error", JOptionPane.ERROR_MESSAGE);
                }
                  
            
            
            
        
            db.Desconectar();
            
            
            
        }else{
            
            Utils.showDialog(this, "Debe completar todos los campos", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

