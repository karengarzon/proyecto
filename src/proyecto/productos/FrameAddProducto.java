    private void actualizarUtilidad(JTextField etUtilidad, JTextField etPrecioVenta) {
        
        
        if(Utils.checkString(etCostoProducto.getText())){
            float costo = Float.parseFloat(etCostoProducto.getText());
        
        if(Utils.checkString(etUtilidad.getText())){
              float utilidad = Float.parseFloat(etUtilidad.getText());
        
        float res = costo+((costo*utilidad)/100);
        
        etPrecioVenta.setText(""+res);
        }else{
            etPrecioVenta.setText("");
        }
        }
        
        
             
      
        
        
        
        
    }
    
